package introducao;

public class Aula02TiposPrimitivos {
	public static void main(String[] args) {
		//int, double, float, boolean, char, long, byte
		
		int age = 10;
		double salary = 500;
		float result = 15;
		char valueAscii = 87;
		byte age2 = 20;
		short age3 = 30;
		boolean verdade = true;
		boolean falso = false;
		String name = "Afonso";
		System.out.println("int: " + age);
		System.out.println("double: " + salary);
		System.out.println("float: " + result);
		System.out.println("char: " + valueAscii);
		System.out.println("byte: " + age2);
		System.out.println("short: " + age3);
		System.out.println("boolean verdadeiro: " + verdade);
		System.out.println("boolean falso: " + falso);
		System.out.println("String: " + name);
		
	}
}
 